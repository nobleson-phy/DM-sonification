def load_fiducial_dms(path: Path) -> dict:
    """Parse a fiducial-DM table → {display_name: float}."""
    fid = {}
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.lower().startswith("pulsar"):
                continue
            parts = re.split(r"\s+", line)
            if len(parts) < 2:
                continue
            name = parts[0].replace("\u2212", "-")  # unicode minus → ASCII
            try:
                fid[name] = float(parts[1])
            except ValueError:
                continue
    return fid


def lookup_fid(fid: dict, file_pulsar: str):
    """Match a filename pulsar token (e.g. 'J0030_0451') to a fiducial entry."""
    if file_pulsar in fid:
        return fid[file_pulsar]
    plus = file_pulsar.replace("_", "+")
    if plus in fid:
        return fid[plus]
    minus = file_pulsar.replace("-", "\u2212")
    if minus in fid:
        return fid[minus]
    return None


def process_data(data_dir: Path, fiducial_path: Path) -> dict:
    """Read timeseries files + fiducial table, return JSON-serialisable dict."""
    fid = load_fiducial_dms(fiducial_path)
    if not fid:
        sys.exit(f"Error: no fiducial DM entries found in {fiducial_path}")

    pat = re.compile(r"^(J[0-9_+\-]+)_DM_timeseries_(B[0-9]+)\.txt$")
    pulsars: dict = {}

    for fname in sorted(os.listdir(data_dir)):
        m = pat.match(fname)
        if not m:
            continue
        file_pulsar = m.group(1)
        band        = m.group(2)

        fid_dm = lookup_fid(fid, file_pulsar)
        if fid_dm is None:
            print(f"Warning: no fiducial DM for '{file_pulsar}', skipping {fname}",
                  file=sys.stderr)
            continue

        display = file_pulsar.replace("_", "+")
        pts = []
        with open(data_dir / fname, encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if not line or line.startswith("#"):
                    continue
                parts = line.split()
                if len(parts) < 3:
                    continue
                try:
                    mjd = float(parts[0])
                    dm  = float(parts[1])
                    err = float(parts[2])
                except ValueError:
                    continue
                pts.append({
                    "mjd":      int(round(mjd)),
                    "residual": round(dm - fid_dm, 8),
                    "error":    err,
                })
        pts.sort(key=lambda d: d["mjd"])

        pulsars.setdefault(display, {"fid_dm": fid_dm, "bands": {}})
        pulsars[display]["bands"][band] = pts

    if not pulsars:
        sys.exit("Error: no timeseries files matched — check data_dir and filename convention")

    all_resid, all_err = [], []
    for info in pulsars.values():
        for pts in info["bands"].values():
            for d in pts:
                all_resid.append(d["residual"])
                all_err.append(d["error"])
    all_resid.sort()
    n = len(all_resid)

    stats = {
        "n_pulsars": len(pulsars),
        "n_points":  n,
        "resid_min": min(all_resid),
        "resid_max": max(all_resid),
        "err_min":   min(all_err),
        "err_max":   max(all_err),
        "err_median": sorted(all_err)[n // 2],
    }

    return {
        "stats": stats,
        "pulsars": [
            {"name": name, "fid_dm": info["fid_dm"], "bands": info["bands"]}
            for name, info in sorted(pulsars.items())
        ],
    }


# ---------------------------------------------------------------------------
# HTML generation
# ---------------------------------------------------------------------------

def build_html(data: dict) -> str:
    template = base64.b64decode(_TEMPLATE_B64).decode("utf-8")
    placeholder = "__PULSAR_DATA_JSON__"
    if placeholder not in template:
        sys.exit("Error: embedded template is corrupt (placeholder missing)")
    data_json = json.dumps(data, separators=(",", ":"))
    return template.replace(placeholder, data_json, 1)


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def parse_args():
    p = argparse.ArgumentParser(
        description="Generate a self-contained InPTA pulsar DM sonifier HTML file.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument("data_dir",    type=Path, help="Directory containing DM timeseries files")
    p.add_argument("output_html", type=Path, help="Output HTML file path")
    p.add_argument(
        "--fiducial", type=Path, default=None, metavar="FILE",
        help="Path to fiducial DM table (default: Pulsar_Fiducial_DM.txt inside data_dir)",
    )
    return p.parse_args()


def main():
    args = parse_args()

    data_dir = args.data_dir.resolve()
    if not data_dir.is_dir():
        sys.exit(f"Error: data_dir '{data_dir}' is not a directory")

    fiducial_path = (args.fiducial or data_dir / "Pulsar_Fiducial_DM.txt").resolve()
    if not fiducial_path.is_file():
        sys.exit(
            f"Error: fiducial DM file not found at '{fiducial_path}'\n"
            f"       Use --fiducial to specify its location"
        )

    out_path = args.output_html.resolve()
    out_path.parent.mkdir(parents=True, exist_ok=True)

    print(f"Reading data from : {data_dir}")
    print(f"Fiducial DM file  : {fiducial_path}")
    data = process_data(data_dir, fiducial_path)
    s = data["stats"]
    print(f"Loaded {s['n_pulsars']} pulsars, {s['n_points']} data points")

    html = build_html(data)
    out_path.write_text(html, encoding="utf-8")
    size_kb = out_path.stat().st_size / 1024
    print(f"Written           : {out_path}  ({size_kb:.1f} KiB)")


if __name__ == "__main__":
    main()
